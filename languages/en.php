/**
 * Get translation for a key
 * @param string $key
 * @param string $default
 * @return string
 */
function __($key, $default = null) {
    global $translations;
    return $translations[$key] ?? $default ?? $key;
}

/**
 * Echo translation for a key
 * @param string $key
 * @param string $default
 */
function _e($key, $default = null) {
    echo __($key, $default);
}
